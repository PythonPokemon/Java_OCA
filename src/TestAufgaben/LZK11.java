package TestAufgaben;

public class LZK11 {
    public static void main(String[] args) {
        int i, j = 0;
        i = (3 * 2 + 4 + 5); // 3 * 2 = 6 + 4 + 5 = 15
        j = (3 * (2 + 4) + 5); // 3 * 6 = 18 + 5 = 23
        System.out.println("i: " + i + "\nj: " + j); // gibt das ergebnis von i = 15 | zeilenumbruch \n | gibt das ergebnis von j = 23
    }
}
