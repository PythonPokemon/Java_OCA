package LF11aV2_LZK_JAVA;

public class LZK17 {
    public static void main(String[] args) {

        for (int row = 4; row > 0; row--) {
            int col = row;
            while (col <= 4) {
                System.out.print(col);
                col++;
            }
            System.out.println();
        }
    }
}