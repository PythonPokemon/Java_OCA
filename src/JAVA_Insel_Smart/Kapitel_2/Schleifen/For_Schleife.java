/*
Die for-Schleife

Die for-Schleife ist besonders nützlich, wenn Sie eine Schleife mit einer festen Anzahl von Iterationen benötigen:
* */
package JAVA_Insel_Smart.Kapitel_2.Schleifen;

public class For_Schleife {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }

    }
}
