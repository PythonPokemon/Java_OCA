package JAVA_Insel_Smart.OCA.Kapitel_5;

public class Zeichen_in_Großbuchstaben_Kleinbuchstaben_konvertieren {
    public static void main(String[] args) {
        // Zeichen in Großbuchstaben/Kleinbuchstaben konvertieren
        char upper = Character.toUpperCase('a'); // umwandlung von klein zu großbuchstaben
        char lower = Character.toLowerCase('A'); // umwandlung von groß zu kleinbuchstaben

        System.out.println(upper);
        System.out.println(lower);
    }
}
