package JAVA_Insel_Smart.OCA.Kapitel_5;

public class Zugriff_auf_bestimmtes_Zeichen {
    public static void main(String[] args) {

        // Zugriff auf ein bestimmtes Zeichen mit charAt(int)
        // Ein bestimmtes Zeichen in einem String kann mit der Methode charAt(int index) abgerufen werden.
        char ch = "Hello".charAt(0); // gibt zeichen iim Index 0 aus'H'

        System.out.println(ch);
    }
}
