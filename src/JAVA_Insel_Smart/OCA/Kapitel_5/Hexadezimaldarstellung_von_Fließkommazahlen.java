package JAVA_Insel_Smart.OCA.Kapitel_5;

public class Hexadezimaldarstellung_von_Fließkommazahlen {
    public static void main(String[] args) {

        // Mit %a kann eine Fließkommazahl im Hexadezimalformat ausgegeben werden.
        double d = 0.75;
        System.out.printf("%a", d);  // Ausgabe: 0x1.8p-1

    }
}
