/*
Die Alternative mit einer if-else-Anweisung wählen

Mit der if-else-Anweisung können Sie eine Alternative angeben, die ausgeführt wird, wenn die Bedingung falsch ist:

int zahl = 3;
if (zahl > 5) {
    System.out.println("Zahl ist größer als 5");
} else {
    System.out.println("Zahl ist nicht größer als 5");
}
* */
package JAVA_Insel_Smart.OCA.Kapitel_2.IF_Else;

public class IF_Else_Anweisung {
    public static void main(String[] args) {
        int zahl = 10;

        if (zahl > 5) {
            System.out.println("Zahl ist größer als 5");
        } else {
            System.out.println("Zahl ist nicht größer als 5");
        }


    }
}
