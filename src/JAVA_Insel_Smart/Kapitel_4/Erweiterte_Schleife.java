package JAVA_Insel_Smart.Kapitel_4;

public class Erweiterte_Schleife {
    public static void main(String[] args) {
        int[] numbers = { 2, 3, 5, 7, 11, 13 };

        for (int number : numbers) {    // number iteriert durch numbers, von element zu element
            System.out.println(number); // jedes Mal, wenn number durch ein neues element iteriert, wird es ausgegeben
        }

    }
}
