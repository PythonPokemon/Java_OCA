package ImperativeSprachkonstrukte;

/**
 * Lernziel:
 * - Klassen > Methoden > Code
 * - main (...) mit kleinbuchtsaben
 * - Bezeichner bsp. Class Bezeichner {} mit großbuchstaben
 * - Groß-/Kleinschreibung
 * - Rservierte Schlüsselwörter
 * - Modifizierer
 * - Einrücken
 * variable bsp: arg = ist eine parameter variable
 * Dateiname und Class name muss identisch sein! bsp: class Application = Application.java
 */

class Application {
    // public static void main = sind eingebaute schlüsselwörter
    // das ist eine Methode = main
    public static void main(String[] args) {
        System.out.println("Hallo Welt");// System = Klasse groß | out = variable | printIn = Methode
    }
}
