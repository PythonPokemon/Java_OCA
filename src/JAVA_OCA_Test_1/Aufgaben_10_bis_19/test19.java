/*
* Die Reihenfolge von package, imports und class ist in Java syntaktisch wichtig, jedoch nicht in dem Sinne,
* dass das Programm nicht kompiliert oder funktioniert, wenn sie nicht genau eingehalten wird.
* Stattdessen handelt es sich um eine Konvention und eine gute Praxis, die von den meisten Java-Entwicklern befolgt wird,
* um den Code lesbarer und organisierter zu gestalten.

Hier sind einige Gründe, warum die Reihenfolge beachtet werden sollte:

Lesbarkeit:
* Die Einhaltung einer konsistenten Reihenfolge erleichtert es anderen Entwicklern (einschließlich dir selbst), den Code schnell zu verstehen und zu navigieren.
*
Organisation:
* Durch die strikte Einhaltung einer Reihenfolge werden die verschiedenen Teile des Codes klar voneinander getrennt, was die Strukturierung und Wartung des Codes erleichtert.
*
Übersichtlichkeit:
* Das Einhalten einer festgelegten Reihenfolge erleichtert es, bestimmte Teile des Codes schnell zu finden, z. B. den Paketnamen, importierte Klassen oder die Hauptklasse.
*
* ----------------------------------------------------------------------------------------------------------------------
*-------------------------------Die empfohlene Reihenfolge ist normalerweise--------------------------------------------
* package:
* Dies sollte immer an erster Stelle im Code stehen und den Namen des Pakets angeben, dem die Klasse gehört.
*
* imports:
* Danach folgen alle import-Anweisungen, die benötigt werden, um auf Klassen aus anderen Paketen zuzugreifen.
*
* class:
* Schließlich kommt die Deklaration der Klasse, die den eigentlichen Code enthält.
*-----------------------------------------------------------------------------------------------------------------------
* Es ist wichtig zu beachten, dass Java keine strikte Reihenfolge erzwingt, und das Programm wird auch funktionieren,
* wenn die Reihenfolge geändert wird.
* Dennoch ist es eine gute Praxis, die Konventionen einzuhalten, um den Code konsistent und leichter lesbar zu machen.
*
* */

