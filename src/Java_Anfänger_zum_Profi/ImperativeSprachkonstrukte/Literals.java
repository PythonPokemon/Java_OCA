package Java_Anfänger_zum_Profi.ImperativeSprachkonstrukte;

public class Literals {
    public static void main(String[] args) {
        System.out.println(true);
        System.out.println(false);
        System.out.println(1233);
        System.out.println(1.765);
        System.out.println(10E1); // 10 hoch 1 = 100
        System.out.println(0.1 + 0.1); // = 0.2
        System.out.println(0.1 + 0.1 + 0.1); // = etwas ungenau 0.30000000000000004
        System.out.println('\n'); // Zeilenumbruch
        System.out.println(123);
        System.out.println("'\""); //mit backslash \ zeichenkette string trennen
        System.out.println(0x0); // = 0
        System.out.println(0x9); // = 9
        System.out.println(0xa); // = 10
        System.out.println(0xb); // = 11
        System.out.println(0xc); // = 12
        System.out.println(0xd); // = 13
        System.out.println(0xe); // = 14
        System.out.println(0xf); // = 15
        System.out.println(0x10); // = 16
        System.out.println(0b11110000_00001111); // binär
        System.out.println(1_2_4);

    }
}
